def opcode(input_list: list):
    #rounds = int(len(input_list)//5)
    for i in range(0, len(input_list), 4):  
            if input_list[i] == 99:
                break
            if input_list[i] == 1:
                num1 = input_list[i+1]
                num1 = input_list[num1]
                num2 = input_list[i+2]
                num2 = input_list[num2]
                num3 = input_list[i+3]
                input_list[num3] = num1 + num2
            elif input_list[i] == 2:
                num1 = input_list[i+1]
                num1 = input_list[num1]
                num2 = input_list[i+2]
                num2 = input_list[num2]
                num3 = input_list[i+3]
                input_list[num3] = num1 * num2
            else:
                print("Program Completed")
    return(input_list)

